package main

import "fmt"

func main() {
	// Numeric operators
	fmt.Println(20 + 10)
	fmt.Println(20 - 10)
	fmt.Println(20 / 10)
	fmt.Println(20 * 10)
	fmt.Println(20 % 10)

	// Boolean operators
	fmt.Println(true && true)
	fmt.Println(true && false)
	fmt.Println(true || true)
	fmt.Println(true || false)
	fmt.Println(!true)
}
